    private static void assignPaddingBorder(JTextField textField, Color color) {
        textField.setBorder(BorderFactory.createCompoundBorder(
                BorderFactory.createLineBorder(color),
                BorderFactory.createEmptyBorder(2, 5, 0, 5)
        ));
    }

    private static void assignPlaceholderFocusListener(JTextField textField, String placeholder) {
        textField.setText(placeholder);

        textField.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (textField.getText().equals(placeholder)) {
                    textField.setText("");
                    textField.setForeground(Color.black);
                    textField.setBackground(Color.white);
                    assignPaddingBorder(textField, BORDER_BLACK_SOFT);
                }
            }

            @Override
            public void focusLost(FocusEvent e) {
                if (textField.getText().isEmpty()) {
                    textField.setText(placeholder);
                    textField.setForeground(Color.gray);

                }
            }
        });

    }

    private static void assignHoverButton(JButton button) {
        Color backgroundColorButton = button.getBackground();

        button.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                button.setBorder(
                        BorderFactory.createEtchedBorder(Color.GRAY, null)
                );
            }

            @Override
            public void mouseExited(MouseEvent e) {
                button.setBorder(
                        BorderFactory.createEtchedBorder(null, null)
                );
            }
        });

    }

    private void initComponentsCalendar() {
        selectedDate = null;

        // Obtener la fecha actual
        Calendar calendar = Calendar.getInstance();

        // Incrementar la fecha actual en un día
        calendar.add(Calendar.DAY_OF_MONTH, 1);

        // Obtener la fecha mínima como Date
        Date minDate = calendar.getTime();
        jCalendarDate.setMinSelectableDate(minDate);

        // Ocultar los nombres de los días de la semana
        jCalendarDate.setWeekOfYearVisible(false);

        // Obtener el año actual
        int yearActual = Calendar.getInstance().get(Calendar.YEAR);

        // Acceder al JYearChooser dentro del JDateChooser
        JYearChooser yearChooser = jCalendarDate.getYearChooser();

        // Establecer el año mínimo en el JYearChooser
        yearChooser.setMinimum(yearActual);

        jCalendarDate.addPropertyChangeListener("calendar", (PropertyChangeEvent evt) -> {
            if ("calendar".equals(evt.getPropertyName())) {
                // Obtén el valor del nuevo calendario seleccionado
                GregorianCalendar selectedCalendar = (GregorianCalendar) evt.getNewValue();

                // Comprueba si la fecha seleccionada es posterior a la fecha actual
                if (selectedCalendar.getTime().after(new Date())) {
                    // Convierte el calendario a un objeto Date
                    selectedDate = selectedCalendar.getTime();

                    // Crea un formato de fecha personalizado
                    SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

                    // Formatea la fecha como una cadena de texto
                    String formattedDate = dateFormat.format(selectedDate);

                    // Si es posterior, muestra la fecha en un JLabel y cambia el color a azul
                    jLabelDateChoosen.setText(formattedDate);
                    jLabelDateChoosen.setForeground(Color.BLUE); // Cambia el color del texto a azul
                }
            }
        });

    }

    private void initComponentNumberPersons() {
        // Inicializa las variables de cantidad de personas
        numPersons = "2";

        // Define un arreglo de botones
        JButton[] listButtons = {
            jButtonNumberPersonsOne,
            jButtonNumberPersonsTwo,
            jButtonNumberPersonsThree,
            jButtonNumberPersonsFour,
            jButtonNumberPersonsFive,
            jButtonNumberPersonsSix,
            jButtonNumberPersonsSeven,
            jButtonNumberPersonsEight};

        // Deshabilita inicialmente el spinner
        jSpinnerNumberPersonsOther.setEnabled(false);

        // ActionListener común para todos los botones
        ActionListener buttonClickListener = (ActionEvent e) -> {
            // Reinicia la selección del checkBox de otra cantidad de personas y deshabilita el spinner
            jCheckBoxNumberPersonsOther.setSelected(false);
            jSpinnerNumberPersonsOther.setEnabled(false);

            // Obtiene el botón seleccionado
            JButton selectedButton = (JButton) e.getSource();
            numPersons = selectedButton.getText();

            // Restaura el aspecto de todos los botones
            for (JButton button : listButtons) {
                button.setBackground(WHITE);
                button.setForeground(BLACK);
            }

            // Aplica el aspecto seleccionado al botón actual
            selectedButton.setBackground(BACKGROUND_BLACK_SOFT);
            selectedButton.setForeground(WHITE);
        };

        // Agregar el ActionListener común a los botones
        for (JButton button : listButtons) {
            button.addActionListener(buttonClickListener);
            assignHoverButton(button);
        }

        // ChangeListener común para jCheckBox y jSpinner
        ChangeListener checkBoxSpinnerChangeListener = (ChangeEvent e) -> {
            // Verifica si la casilla de verificación está seleccionada
            boolean checkBoxSelected = jCheckBoxNumberPersonsOther.isSelected();
            jSpinnerNumberPersonsOther.setEnabled(checkBoxSelected);

            // Actualiza la cantidad de personas
            numPersons = checkBoxSelected
                    ? jSpinnerNumberPersonsOther.getValue().toString()
                    : "";
        };

        // Agregar el ChangeListener común a jCheckBox y jSpinner
        jCheckBoxNumberPersonsOther.addChangeListener(checkBoxSpinnerChangeListener);
        jSpinnerNumberPersonsOther.addChangeListener(checkBoxSpinnerChangeListener);

        // ActionListener para la casilla de verificación del checkBox de otra cantidad de personas
        jCheckBoxNumberPersonsOther.addActionListener((ActionEvent e) -> {
            // Restaura el aspecto de todos los botones
            for (var button : listButtons) {
                button.setBackground(WHITE);
                button.setForeground(BLACK);
            }

            // Actualiza la cantidad de personas en función de la casilla de verificación
            if (jCheckBoxNumberPersonsOther.isSelected()) {
                numPersons = jSpinnerNumberPersonsOther.getValue().toString();
            } else {
                numPersons = "";
            }
        });
    }

