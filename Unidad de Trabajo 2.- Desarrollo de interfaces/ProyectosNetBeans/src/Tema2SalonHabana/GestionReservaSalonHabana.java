    private void initComponentTypeEvent() {
        event = "Banquete";

        // Define un arreglo de botones
        JButton[] listButtons = {
            jButtonFeast,
            jButtonDayWork,
            jButtonCongress
        };

        // ActionListener común para todos los botones
        ActionListener buttonClickListener = (ActionEvent e) -> {
            // Obtiene el botón seleccionado
            JButton selectedButton = (JButton) e.getSource();
            event = selectedButton.getText();

            // Restaura el aspecto de todos los botones
            for (JButton button : listButtons) {
                button.setBackground(WHITE);
                button.setForeground(BLACK);

                jSpinnerRoomDays.setEnabled(false);
            }

            // Aplica el aspecto seleccionado al botón actual
            selectedButton.setBackground(BACKGROUND_BLACK_SOFT);
            selectedButton.setForeground(WHITE);

            if (event.equals("Congreso")) {
                event = "Congreso (0 días)";
                jButtonRoomNo.setEnabled(true);
                jButtonRoomYes.setEnabled(true);

                jButtonRoomNo.setBackground(BACKGROUND_BLACK_SOFT);
                jButtonRoomNo.setForeground(WHITE);

                jButtonRoomYes.setBackground(WHITE);
                jButtonRoomYes.setForeground(BLACK);
            } else {
                jButtonRoomNo.setEnabled(false);
                jButtonRoomYes.setEnabled(false);

                jButtonRoomNo.setBackground(WHITE);
                jButtonRoomNo.setForeground(BLACK);

                jButtonRoomYes.setBackground(WHITE);
                jButtonRoomYes.setForeground(BLACK);
            }
        };

        JButton[] listButtonsNoYes = {
            jButtonRoomNo,
            jButtonRoomYes
        };

        ActionListener buttonNoYesClickListener = (ActionEvent e) -> {
            // Obtiene el botón seleccionado
            JButton selectedButton = (JButton) e.getSource();

            // Restaura el aspecto de todos los botones
            for (JButton button : listButtonsNoYes) {
                button.setBackground(WHITE);
                button.setForeground(BLACK);
            }

            // Aplica el aspecto seleccionado al botón actual
            selectedButton.setBackground(BACKGROUND_BLACK_SOFT);
            selectedButton.setForeground(WHITE);

            if (selectedButton.getText().equals("Sí")) {
                jSpinnerRoomDays.setEnabled(true);
                
                event = "Congreso (" + jSpinnerRoomDays.getValue().toString() + " días)";
            } else {
                jSpinnerRoomDays.setEnabled(false);
                event = "Congreso (0 días)";
            }
            jSpinnerRoomDays.setEnabled(
                    selectedButton.getText().equals("Sí")
            );
        };
        
        jSpinnerRoomDays.addChangeListener((ChangeEvent e) -> {
            event = "Congreso (" + jSpinnerRoomDays.getValue().toString() + " días)";
        });

        // Agregar el ActionListener común a los botones
        for (JButton button : listButtons) {
            button.addActionListener(buttonClickListener);
            assignHoverButton(button);
        }

        for (JButton button : listButtonsNoYes) {
            button.addActionListener(buttonNoYesClickListener);
            assignHoverButton(button);
        }
    }

