    private void jButtonReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReserveActionPerformed
        controlDatesCorrect = true;

        if (jTextField1_1_ClientName.getForeground().equals(Color.gray)) {
            assignPaddingBorder(jTextField1_1_ClientName, RED);
            controlDatesCorrect = false;
        }

        if (jTextField1_2_ClientPhone.getForeground().equals(Color.gray)) {
            assignPaddingBorder(jTextField1_2_ClientPhone, RED);
            controlDatesCorrect = false;
        }

        if (selectedDate == null) {
            jLabelDateChoosen.setForeground(RED);
            controlDatesCorrect = false;
        }

        if (controlDatesCorrect) {
            name = jTextField1_1_ClientName.getText().toString();
            phone = jTextField1_2_ClientPhone.getText().toString();

            reserve = new Reserve(name, phone, numPersons, selectedDate, cousine, event);
            salonHabana.aniadirReserva(reserve);

            dispose();
        }
    }//GEN-LAST:event_jButtonReserveActionPerformed

